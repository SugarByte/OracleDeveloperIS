-- MAIN
CREATE TABLE x_documents (
    doc_id        NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    staging_id    NUMBER,
    reason        VARCHAR2(500),
    email         VARCHAR2(200),
    has_rep       CHAR(1) CHECK (has_rep IN ('Y','N')),
    created_at    DATE DEFAULT SYSDATE
);

-- TAXPAYER
CREATE TABLE x_taxpayers (
    taxpayer_id   NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    doc_id        NUMBER NOT NULL,
    name          VARCHAR2(200),
    egn           VARCHAR2(10),
    type          NUMBER(1) CHECK (type IN (1,2)),
    bulstat       VARCHAR2(10),
    address       VARCHAR2(500),
    CONSTRAINT fk_taxpayer_doc FOREIGN KEY (doc_id)
        REFERENCES x_documents(doc_id)
);

-- REPRESENTATIVE
CREATE TABLE x_representatives (
    rep_id        NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    doc_id        NUMBER NOT NULL,
    name          VARCHAR2(200),
    egn           VARCHAR2(10),
    type          NUMBER(1) CHECK (type IN (1,2)),
    CONSTRAINT fk_rep_doc FOREIGN KEY (doc_id)
        REFERENCES x_documents(doc_id)
);

-- DOCENUM
CREATE TABLE x_doc_enums (
    docenum_id    NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    doc_id        NUMBER NOT NULL,
    ext_id        VARCHAR2(100),
    CONSTRAINT fk_docenum_doc FOREIGN KEY (doc_id)
        REFERENCES x_documents(doc_id)
);

-- FIELDS
CREATE TABLE x_doc_fields (
    field_id      NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    docenum_id    NUMBER NOT NULL,
    name          VARCHAR2(200),
    value         VARCHAR2(500),
    CONSTRAINT fk_field_docenum FOREIGN KEY (docenum_id)
        REFERENCES x_doc_enums(docenum_id)
);

CREATE SEQUENCE x_seq_documents START WITH 1;
CREATE SEQUENCE x_seq_docenum START WITH 1;

CREATE TABLE xml_staging (
    id           NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    xml_data     CLOB,
    status       VARCHAR2(20) DEFAULT 'NEW',
    error_msg    VARCHAR2(1000),
